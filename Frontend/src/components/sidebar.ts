export function renderSidebar(container: HTMLElement): void {
  const role = getUserRole();
  const currentHash = location.hash.slice(1) || "/dashboard";

  container.innerHTML = `
    <div class="flex items-center gap-2 px-4 h-16 border-b border-gray-200">
      <div class="w-8 h-8 bg-indigo-600 rounded-lg flex items-center justify-center">
        <span class="text-white font-bold text-sm">R</span>
      </div>
      <span class="font-bold text-gray-900 text-lg">Refaxio</span>
    </div>
    <nav class="flex-1 overflow-y-auto py-4" id="sidebar-nav"></nav>
  `;

  const nav = container.querySelector("#sidebar-nav")!;

  for (const section of sections) {
    const visibleItems = section.items.filter(
      (item) => !item.roles || item.roles.includes(role),
    );
    if (visibleItems.length === 0) continue;

    const sectionEl = document.createElement("div");
    sectionEl.className = "mb-4";
    sectionEl.innerHTML = `<p class="px-4 text-xs font-semibold text-gray-400 uppercase tracking-wider mb-1">${section.title}</p>`;

    for (const item of visibleItems) {
      const isActive = currentHash === item.hash;
      const link = document.createElement("a");
      link.href = `#${item.hash}`;
      link.className = `flex items-center gap-3 px-4 py-2 mx-2 rounded-lg text-sm transition-colors ${
        isActive
          ? "bg-indigo-50 text-indigo-700 font-medium"
          : "text-gray-600 hover:bg-gray-100 hover:text-gray-900"
      }`;
      link.innerHTML = `${item.icon}<span>${item.label}</span>`;
      link.addEventListener("click", (e) => {
        e.preventDefault();
        navigate(`#${item.hash}`);
      });
      sectionEl.appendChild(link);
    }

    nav.appendChild(sectionEl);
  }
}
